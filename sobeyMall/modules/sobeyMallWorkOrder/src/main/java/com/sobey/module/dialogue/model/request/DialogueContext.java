package com.sobey.module.dialogue.model.request;

/**
 * @author lgc
 * @date 2020年4月28日 上午11:23:22
 */
public class DialogueContext {
	
	private String content;
	private String typeCode;
	private String sourceUserCode;
	private String targetUserCode;
	private String workOrderNum;
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @return the typeCode
	 */
	public String getTypeCode() {
		return typeCode;
	}
	/**
	 * @param typeCode the typeCode to set
	 */
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	/**
	 * @return the sourceUserCode
	 */
	public String getSourceUserCode() {
		return sourceUserCode;
	}
	/**
	 * @param sourceUserCode the sourceUserCode to set
	 */
	public void setSourceUserCode(String sourceUserCode) {
		this.sourceUserCode = sourceUserCode;
	}
	/**
	 * @return the targetUserCode
	 */
	public String getTargetUserCode() {
		return targetUserCode;
	}
	/**
	 * @param targetUserCode the targetUserCode to set
	 */
	public void setTargetUserCode(String targetUserCode) {
		this.targetUserCode = targetUserCode;
	}
	/**
	 * @return the workOrderNum
	 */
	public String getWorkOrderNum() {
		return workOrderNum;
	}
	/**
	 * @param workOrderNum the workOrderNum to set
	 */
	public void setWorkOrderNum(String workOrderNum) {
		this.workOrderNum = workOrderNum;
	}
	
	
	
	

}
